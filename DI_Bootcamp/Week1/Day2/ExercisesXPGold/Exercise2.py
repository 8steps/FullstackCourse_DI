Month = int(input("Please type a month so we could check its season:"))

if 3<= Month <=5:
    print("Ohh look it's Spring!")
elif 6<= Month <=8:
    print("Well it's Summer!")
elif 9<= Month <=11:
    print("It's Going to be Autumn at this month!")
else:
    print("It's the cold Winter this time !")
    